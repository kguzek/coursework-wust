class JavaSamochod {
    private String marka;
    private String model;
    private int rokProdukcji;
    private double cena;

    public JavaSamochod(String marka, String model, int rokProdukcji, double cena) {
        this.marka = marka;
        this.model = model;
        this.rokProdukcji = rokProdukcji;
        this.cena = cena;
    }
}